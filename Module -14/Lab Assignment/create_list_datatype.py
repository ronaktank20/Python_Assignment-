#Write a Python program to create a list with elements of multiple data types (integers, 
#strings, floats, etc.). 

# Integer list
lst1 = [10, 20, 30, 40]

# Float list
lst2 = [2.5, 12.56, 6.9, 4.8]

# String list
lst3 = ['monak', 'ronak', 'ustav']

print("Integer List:")
for i in lst1:
    print(i)

print("\nFloat List:")
for i in lst2:
    print(i)

print("\nString List:")
for i in lst3:
    print(i)
