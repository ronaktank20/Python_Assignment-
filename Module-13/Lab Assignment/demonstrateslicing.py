# Write a Python program to demonstrate string slicing.

str = "tops technologies"

print("String:",str)

print("Slice [0:4]:",str[0:4])  

print("Slice [5:]:",str[5:])    

print("Last 2 characters:",str[-2:])  

print("Reversed String:",str[::-1])   

print("alternate character:",str[::2])  
