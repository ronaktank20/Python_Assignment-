#Practical Example 7: Write a Python program to calculate grades based on percentage using 
#if-else ladder. 

marks =int(input("enter the marks :"))
if marks >= 90:
    print("grade A")

elif marks >= 80:
    print("grade B")

elif marks >= 70: 
    print("grade C")

elif marks >= 60: 
    print("grade D")

else :
    print("grade E")

